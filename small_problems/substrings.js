// alternative approach using a for loop
function substrings2(string) {
  let substrings = [];
  for (let startIndex = 0; startIndex < string.length; startIndex += 1) {
    let substring = string.substring(startIndex);
    substrings = substrings.concat(leadingSubstrings(substring));
  }

  return substrings;
}

// my approach
function substrings(string) {
  return leadingSubstrings(trailingSubstrings(string));
}

function trailingSubstrings(string) {
  let substrings = [];
  for (let start = 0; start < string.length; start += 1) {
    substrings.push(string.slice(start));
  }

  return substrings;
}

function leadingSubstrings(string) {
  let substrings = [];
  for (let length = 1; length <= string.length; length += 1) {
    substrings.push(string.slice(0, length));
  }

  return substrings;
}

